{
    LSC_LOG_BEGIN("");
    MINT32 i = 0;
    m_pLsc = ILscMgr::getInstance(static_cast<ESensorDev_T>(m_u4SensorDev));

    for (i = 0; i < CT_NUM; i++)
    {
        m_pTbl[i] = new ILscTbl(ILscTbl::GAIN_FLOAT);
    }
    LSC_LOG_END("");
    return MTRUE;
}

