MBOOL
OpenShading::
perframe(const ILscMgr::TSF_INPUT_INFO_T& rInputInfo)
{
    if (m_fgOnOff)
    {
        LSC_LOG("sensor(%d), cmd(%d), magic(%d)", m_u4SensorDev, rInputInfo.eCmd, rInputInfo.u4FrmId);
        MINT32 i = 0;
        ILscTbl rTblRto(ILscTbl::GAIN_FLOAT);
        ILscTbl rTblTmp(ILscTbl::HWTBL);

        MINT32 i4CCTForce, i4RatioForce;
        GET_PROP("debug.openshading.cct", "-1", i4CCTForce);
        GET_PROP("debug.openshading.ratio", "-1", i4RatioForce);

        MUINT32 u4CCT = (i4CCTForce == -1) ? rInputInfo.rAwbInfo.m_u4CCT : i4CCTForce;
        const MUINT32 u4ThL = 3257;
        const MUINT32 u4Th0 = 3484;
        const MUINT32 u4Th1 = 4673;
        const MUINT32 u4ThH = 5155;

        if (u4CCT < u4ThL)
        {
            rTblRto = *m_pTbl[0];
            LSC_LOG("CT(%d), idx(0.000)", u4CCT);
        }
        else if (u4CCT < u4Th0)
        {
            // intp 0, 1
            rTblRto = *m_pTbl[0];
            MFLOAT* pDest = static_cast<MFLOAT*>(rTblRto.editData());
            const MFLOAT* pSrc = static_cast<const MFLOAT*>(m_pTbl[1]->getData());
            MFLOAT fRto = (MFLOAT)(u4CCT - u4ThL) / (u4Th0 - u4ThL);
            if (i4RatioForce == -2)
            {
                fRto = (MFLOAT)(rInputInfo.u4FrmId & 0x1F) / 32;
            }
            else if (i4RatioForce != -1)
            {
                fRto = (MFLOAT) i4RatioForce / 32;
            }
            MFLOAT fRto_1 = 1.0f - fRto;
            for (i = 0; i < 17*17*4; i++)
            {
                MFLOAT fVal_b = *pSrc++;
                MFLOAT& fVal_a = *pDest;
                fVal_a = fRto_1*fVal_a + fRto*fVal_b;
                pDest++;
            }
            LSC_LOG("CT(%d), idx(%3.3f)", u4CCT, fRto);
        }
        else if (u4CCT < u4Th1)
        {
            rTblRto = *m_pTbl[1];
            LSC_LOG("CT(%d), idx(1.000)", u4CCT);
        }
        else if (u4CCT < u4ThH)
        {
            // intp 1, 2
            rTblRto = *m_pTbl[1];
            MFLOAT* pDest = static_cast<MFLOAT*>(rTblRto.editData());
            const MFLOAT* pSrc = static_cast<const MFLOAT*>(m_pTbl[2]->getData());
            MFLOAT fRto = (MFLOAT)(u4CCT - u4Th1) / (u4ThH - u4Th1);
            if (i4RatioForce == -2)
            {
                fRto = (MFLOAT)(rInputInfo.u4FrmId & 0x1F) / 32;
            }
            else if (i4RatioForce != -1)
            {
                fRto = (MFLOAT) i4RatioForce / 32;
            }
            MFLOAT fRto_1 = 1.0f - fRto;
            for (i = 0; i < 17*17*4; i++)
            {
                MFLOAT fVal_b = *pSrc++;
                MFLOAT& fVal_a = *pDest;
                fVal_a = fRto_1*fVal_a + fRto*fVal_b;
                pDest++;
            }
            LSC_LOG("CT(%d), idx(%3.3f)", u4CCT, fRto+1.0f);
        }
        else
        {
            rTblRto = *m_pTbl[2];
            LSC_LOG("CT(%d), idx(2.000)", u4CCT);
        }

        if (!rTblRto.convert(rTblTmp))
        {
            LSC_ERR("Convert error");
        }

        // put to isp_mgr buf
        m_pLsc->syncTbl(rTblTmp);
    }
    return MTRUE;
}

MBOOL
OpenShading::
update(const ILscMgr::TSF_INPUT_INFO_T& rInputInfo)
{
    MBOOL fgRet = MFALSE;

    switch (rInputInfo.eCmd)
    {
    case ILscMgr::E_TSF_CMD_INIT:
        break;
    case ILscMgr::E_TSF_CMD_CHG:
        //fgRet = start(rInputInfo);
        break;
    case ILscMgr::E_TSF_CMD_RUN:
    case ILscMgr::E_TSF_CMD_BATCH:
    case ILscMgr::E_TSF_CMD_BATCH_CAP:
        fgRet = perframe(rInputInfo);
        break;
    }
    return fgRet;
}
