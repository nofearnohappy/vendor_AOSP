package com.android.mms.ui;

import android.content.Context;
import android.util.AttributeSet;
import android.widget.CheckBox;
import android.widget.ImageView;
import android.widget.RelativeLayout;
import android.widget.TextView;
import com.android.mms.R;

/** M:
 * This class manages the view for given conversation.
 */
public class MultiSaveListItem extends RelativeLayout {
    private static final String TAG = "Mms/MultiSaveListItem";

    private TextView mSizeView;

    private TextView mNameView;

    private ImageView mThumbnailView;

    private CheckBox mCheckbox;

    public MultiSaveListItem(Context context) {
        super(context);
    }

    public MultiSaveListItem(Context context, AttributeSet attrs) {
        super(context, attrs);
    }

    @Override
    protected void onFinishInflate() {
        super.onFinishInflate();
        mNameView = (TextView) findViewById(R.id.name);
        mSizeView = (TextView) findViewById(R.id.size);
        mThumbnailView = (ImageView) findViewById(R.id.thumbnail);
        mCheckbox = (CheckBox) findViewById(R.id.checkbox);
    }

    public void clickListItem() {
        if (mCheckbox.isChecked()) {
            unMarkItem();
        } else {
            markItem();
        }
    }

    public void selectItem(boolean select) {
        if (select) {
            markItem();
        } else {
            unMarkItem();
        }
    }

    private void markItem() {
        mCheckbox.setChecked(true);
        setBackgroundResource(R.drawable.list_selected_holo_light);
    }

    private void unMarkItem() {
        mCheckbox.setChecked(false);
        setBackgroundResource(R.drawable.listitem_background);
    }
}
